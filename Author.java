public class Author {

    private String name;
    private String bio;

    //Setter methods
    public void setName(String name) {
        this.name = name;
    }

    public void setBio(String bio) {
        this.bio = bio;
    }

    //Getter methods
    public String getName() {
        return name;
    }

    public String getBio() {
        return bio;
    }
}